class AKS {
	public static void main(String[]args){
		String originalStr = "Akshat"; 
		String akshat = ""; 
	
		
		System.out.println("Akshat"+9+10);
		
		
		
		for (int i = originalStr.length()-1; i >=0; i--) 
		{ 
			akshat =akshat+ originalStr.charAt(i) ;
			
		} 
		System.out.println("Reversed string: "+akshat);
	}

}
